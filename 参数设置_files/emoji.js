function stringToHex(characters, bytesArray, m, n) {
    var emojiStr = '';
    if (characters == null) {
        return "";
    }
    var utfBytes = characters.split('');
    for (var byteIndex = 0; byteIndex < characters.length; byteIndex++) {
        if (bytesArray[m + byteIndex] > 0xFFFF) {
            var h = 0xD800 + (bytesArray[m + byteIndex] >> 10) - 0x40;  // high  
            var l = 0xDC00 + (bytesArray[m + byteIndex] & 0x03FF);      // low  
            emojiStr += h.toString(16);
            emojiStr += l.toString(16);
        }
        else {
            var hexB = characters.charCodeAt(byteIndex).toString(16);
            if (hexB.length == 2) {
                hexB = "00" + hexB;
            }
            else if (hexB.length == 1) {
                hexB = "000" + hexB;
            }
            else if (hexB.length == 3) {
                hexB = "0" + hexB;
            }
            emojiStr += hexB;
        }
    }
    return emojiStr;
}

function matchEmoji(text, bytesArray) {
    var emojiStr = '';
    for (var i = 0; i < text.length; i++) {
        var maxLen = text.length - i;
        var hasContains = false;
        for (var h = 0; h < maxLen; h++) {
            var headMsg = text.substring(i, i + (maxLen - h));
            var escapeStr = stringToHex(headMsg, bytesArray, i, i + (maxLen - h)).toUpperCase();

            if (typeof (EmojiMap[escapeStr]) == "string") {
                i += maxLen - h; //maxLen-h都匹配成功，往后跳
                i = i - 1; //抵消i++
                emojiStr += EmojiMap[escapeStr];
                hasContains = true;
                break;
            }
        }
        if (!hasContains) {
            emojiStr += text.substring(i, i + 1);
        }
    }
    return emojiStr;
}


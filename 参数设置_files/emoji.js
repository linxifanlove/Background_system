function matchEmoji2(text, bytesArray) {

    // 转成16进制字符串
    var textHexStr = stringToHex(text, bytesArray, 0, text.length).toUpperCase();
    var emojiStr = '';

    var byteLength = textHexStr.length / 4;

    var n = 0;
    for (var i = 0; i < byteLength; ) {
        var maxLen = byteLength - i;
        if (maxLen > 11) {
            maxLen = 11;
        }
        var hasContains = false;
        for (var h = 0; h < maxLen; h++) {

            // 从最长的开始匹配
            var escapeStr = textHexStr.substr(i * 4, (maxLen - h) * 4);
            // 匹配上
            if (typeof (EmojiMap[escapeStr]) == "string") {
                emojiStr += EmojiMap[escapeStr];
                i += escapeStr.length / 4;
                n++;
                hasContains = true;
                break;
            }
        }
        if (!hasContains) {
            var c = text.substr(n, 1);
            emojiStr += c;
            n++;
            i++;
        }
    }
    return emojiStr;
}


